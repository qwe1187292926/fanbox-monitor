def normalize_lang(lang: str | None) -> str:
    raw = (lang or "zh-CN").strip()
    if not raw:
        return "zh-CN"
    if raw in SUPPORTED_LANGS:
        return raw
    return _ALIASES.get(raw.replace("_", "-").lower(), "zh-CN")


def is_supported_lang(lang: str | None) -> bool:
    raw = (lang or "").strip()
    if not raw:
        return True
    if raw in SUPPORTED_LANGS:
        return True
    return raw.replace("_", "-").lower() in _ALIASES


def env_lang(name: str = "FANBOX_LANG") -> str:
    return normalize_lang(os.environ.get(name))


def t(locale: str | None, key: str, **kwargs: Any) -> str:
    locale = normalize_lang(locale)
    template = MESSAGES.get(locale, {}).get(key)
    if template is None and locale != "en-US":
        template = MESSAGES["en-US"].get(key)
    if template is None:
        template = MESSAGES["zh-CN"].get(key, key)
    if not kwargs:
        return template
    return template.format(**kwargs)
